    private static Dictionary<string, Dictionary<string, double>> Merge(
        Dictionary<string, Dictionary<string, double>> target,
        Dictionary<string, Dictionary<string, double>> overrides) {
      var result = new Dictionary<string, Dictionary<string, double>>(target);
      foreach (var kvp in overrides) {
        if (result.TryGetValue(kvp.Key, out var existing)) {
          var newEntry = new Dictionary<string, double>(existing);
          foreach (var inner in kvp.Value) newEntry[inner.Key] = inner.Value;
          result[kvp.Key] = newEntry;
        } else {
          result[kvp.Key] = new Dictionary<string, double>(kvp.Value);
        }
      }
      return result;
    }

