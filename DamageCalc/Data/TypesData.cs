    private static readonly Dictionary<string, IType>[] TypesById = BuildTypesById();

    public static IDataTable<IType> GetTable(int gen) {
      if (gen < 1 || gen >= TYPE_CHART.Length) throw new ArgumentOutOfRangeException(nameof(gen));
      return new DataTable<IType>(TypesById[gen]);
    }

    private static Dictionary<string, IType>[] BuildTypesById() {
      var result = new Dictionary<string, IType>[TYPE_CHART.Length];
      for (var gen = 0; gen < TYPE_CHART.Length; gen++) {
        var map = new Dictionary<string, IType>();
        foreach (var kvp in TYPE_CHART[gen]) {
          var type = new TypeData {
            Id = Util.ToId(kvp.Key),
            Name = kvp.Key,
            Kind = DataKinds.Type,
            Effectiveness = new Dictionary<string, double>(kvp.Value),
          };
          map[type.Id] = type;
        }
        result[gen] = map;
      }
      return result;
    }
