    private static readonly Dictionary<string, IAbility>[] AbilitiesById = BuildAbilitiesById();

    public static IDataTable<IAbility> GetTable(int gen) {
      if (gen < 1 || gen >= ABILITIES.Length) throw new ArgumentOutOfRangeException(nameof(gen));
      return new DataTable<IAbility>(AbilitiesById[gen]);
    }

    private static Dictionary<string, IAbility>[] BuildAbilitiesById() {
      var result = new Dictionary<string, IAbility>[ABILITIES.Length];
      for (var gen = 0; gen < ABILITIES.Length; gen++) {
        var map = new Dictionary<string, IAbility>();
        foreach (var name in ABILITIES[gen]) {
          var ability = new Ability {
            Id = Util.ToId(name),
            Name = name,
            Kind = DataKinds.Ability,
          };
          map[ability.Id] = ability;
        }
        result[gen] = map;
      }
      return result;
    }
