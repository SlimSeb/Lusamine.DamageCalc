    private static Dictionary<string, IItem>[] BuildItemsById() {
      var result = new Dictionary<string, IItem>[ITEMS.Length];
      for (var gen = 0; gen < ITEMS.Length; gen++) {
        var map = new Dictionary<string, IItem>();
        foreach (var name in ITEMS[gen]) {
          var item = new Item {
            Id = Util.ToId(name),
            Name = name,
            Kind = DataKinds.Item,
          };

          if (MEGA_STONES.TryGetValue(name, out var megaStone)) {
            item.MegaStone = new Dictionary<string, string>(megaStone);
          }

          if (BERRIES.TryGetValue(name, out var berry)) {
            item.IsBerry = true;
            item.NaturalGift = new NaturalGiftData {
              BasePower = gen < 6 ? berry.power - 20 : berry.power,
              Type = berry.type,
            };
          }

          map[item.Id] = item;
        }
        result[gen] = map;
      }
      return result;
    }

    private static Dictionary<string, Dictionary<string, string>> BuildMegaStones() {
      var map = new Dictionary<string, Dictionary<string, string>>();
      foreach (var kvp in GEN_6_MEGA_STONES) {
        map[kvp.Key] = kvp.Value;
      }
      foreach (var kvp in ZA_MEGA_STONES) {
        map[kvp.Key] = kvp.Value;
      }
      return map;
    }
